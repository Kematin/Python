# operations

# +
print(4 + 5) # 9
print(True + True) # 2 (False = 0, True = 1)
print(2.4 + 1.3) # 3.7
print('Hello ' + 'World') # Hello World

# -
print(3 - 2) # 1
print(False - True) # -1

# *
print(3 * 2) # 6
print('a' * 8) # aaaaaaaa

# /
print(6 / 2) # 3.0

# **
print(2**3) # 8 (extent)

# //
print(5 / 2) # 2.5
print(5 // 2) # 2

# %
print(10 % 3) # 1
print(12 % 5) # 2


# sequence
# 1) ()
# 2) **
# 3) *, /, //, %
# 4) + -
print((1+1)**3 - 54 % 3) # 8


# round

num = 7 / 2
print(round(num)) # 4

num_2 = 1 / 3
print(num_2) # 0.3333333333
print(round(num_2, 3)) # 0.333
